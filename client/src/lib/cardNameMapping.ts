/**
 * Retourne la clé française normalisée à partir d'un nom de carte dans n'importe quelle langue
 */
export function getFrenchKeyFromTranslatedName(cardName: string, language: string): string {
  const languageMap = dailyCardNameMapping[language];
  if (!languageMap) {
    console.warn(`⚠️ Langue non supportée: ${language}`);
    return normalizeCardName(cardName);
  }

  const normalizedInput = normalizeCardName(cardName);
  const frenchKey = languageMap[normalizedInput];

  if (frenchKey) {
    return frenchKey;
  }

  const lowerInput = normalizedInput.toLowerCase();
  const foundEntry = Object.entries(languageMap).find(
    ([key]) => key.toLowerCase() === lowerInput
  );

  if (foundEntry) {
    return foundEntry[1];
  }

  console.warn(`⚠️ Pas de mapping daily pour: ${cardName} (${language})`);
  return normalizedInput;
}

